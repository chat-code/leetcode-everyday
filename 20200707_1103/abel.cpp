using namespace std;
using VI = vector<int>;
using VS = vector<string>;

class Solution {
public:
    int get_s(int n, int p) {
        return (n + 1) * n / 2 * p + n * n * p * (p - 1) / 2;
    }

    vector<int> distributeCandies(int candies, int n) {
        VI res(n, 0);

        int p = 1, tmp;
        for (;; p++) {
            tmp = get_s(n, p);
            if (candies < tmp) break;
        }
        --p;
        candies -= get_s(n, p);

        if (p >= 1) {
            for (int i = 0; i < n; ++i) {
                res[i] = (i + 1) * p + (p - 1) * p / 2 * n;
            }
        }

        for (int i = 0; i < n; ++i) {
            if (candies > 0) {
                int tmp = p * n + (i + 1);
                tmp = min(tmp, candies);
                candies -= tmp;
                res[i] += tmp;
            } else {
                break;
            }
        }
        return res;
    }
};
