/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  int res;

  int dfs(TNP ro) {
    if (!ro) return 2;

    auto l = ro->left, r = ro->right;
    if (!l && !r) {
      return 0;
    }

    auto l1 = dfs(l), r1 = dfs(r);
    if (!l1 || !r1) {
      res++;
      return ro->val = 1;
    }

    if (l1 == 1 || r1 == 1) {
      return ro->val = 2;
    }

    return ro->val = 0;
  }

  int minCameraCover(TreeNode* ro) {
    res = 0;
    dfs(ro);

    if (!ro->val) res++;

    return res;
  }
};
