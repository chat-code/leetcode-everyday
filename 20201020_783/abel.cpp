/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  int res;
  VI tmp;

  void dfs(TNP ro) {
    if (!ro) return;

    auto l = ro->left, r = ro->right;
    auto x = ro->val;

    if (!l && !r) {
      tmp.push_back(x);
      return;
    }

    if (l) dfs(l);
    tmp.push_back(x);
    if (r) dfs(r);
  }

  int minDiffInBST(TreeNode* ro) {
    res = INF;
    dfs(ro);

    for (int i = 1; i < tmp.size(); ++i) {
      res = min(res, tmp[i] - tmp[i - 1]);
    }

    return res;
  }
};
