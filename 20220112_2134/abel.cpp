class Solution {
public:
  int minSwaps(vector<int>& a) {
    int n = a.size(), cnt1 {};

    for (auto &x : a) if (x) cnt1++;

    if (!cnt1 || cnt1 == n) return 0;

    int res = n, cur {};
    for (int i = 0, j = 0; i < n + cnt1; ++i) {
      int i1 = i % n;
      if (a[i1]) cur++;

      if (i >= cnt1) {
        if (a[j++]) cur--;
      }

      res = min(res, cnt1 - cur);
    }

    return res;
  }
};
