class Solution {
public:
  int findNthDigit(int n) {
    ll base = 1, a = 9, len = 1;
    while (n > a * len) {
      n -= a * len;
      a *= 10;
      base *= 10;
      len++;
    }

    if (n % len == 0) {
      int x = base + n / len - 1;
      return x % 10;
    }

    int x = base + n / len;

    string s = to_string(x);
    return s[(n % len) - 1] - '0';
  }
};
