class Solution {
public:
  string reversePrefix(string a, char ch) {
    int idx = -1, n = a.size();

    for (int i = 0; i < n; ++i) {
      if (a[i] == ch) {
        idx = i;
        break;
      }
    }

    for (int i = 0, j = idx; i < j; ++i, --j) swap(a[i], a[j]);

    return a;
  }
};
