class Solution {
public:
  int dominantIndex(vector<int>& a) {
    int n = a.size();
    int maxv = a[0], maxi = 0, secondv = -1;

    for (int i = 1; i < n; ++i) {
      if (a[i] >= maxv) {
        secondv = maxv;
        maxv = a[i];
        maxi = i;
      } else {
        secondv = max(secondv, a[i]);
      }
    }

    if (maxv >= secondv * 2) return maxi;
    return -1;
  }
};
