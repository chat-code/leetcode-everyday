const int N = 65537;
bool vis[N];

class Solution {
public:
  VI res;
  int n;

  void dfs(int cur) {
    res.push_back(cur);
    vis[cur] = true;

    for (int i = 0; i < n; ++i) {
      int tmp = 1 << i, ne = cur ^ tmp;
      if (!vis[ne]) {
        dfs(ne);
        break;
      }
    }
  }

  vector<int> grayCode(int _n) {
    memset(vis, false, sizeof vis);
    res.clear();
    n = _n;

    dfs(0);

    return res;
  }
};
