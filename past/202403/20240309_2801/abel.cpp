int dp[120][13];

class Solution {
public:
  string s1, s2;
  int len;

  int dfs(int pos, int x, bool bdl, bool bdr, bool num) {
    if (pos == len)
      return num;

    if (!bdl && !bdr && num && dp[pos][x] != -1)
      return dp[pos][x];

    int low = 0, up = 9, l1 = s1[pos] - '0', r1 = s2[pos] - '0';
    ll ans = 0;

    if (!num && l1 == 0) {
      ll t1 = dfs(pos + 1, 0, true, false, false) % MOD;
      ans = (ans + t1) % MOD;
      low = 1;
    }

    if (bdl)
      ckmax(low, l1);
    if (bdr)
      ckmin(up, r1);

    For1(i, low, up) {
      int tmp = abs(i - x);
      if (tmp == 1 || !num) {
        ll t1 = dfs(pos + 1, i, bdl && i == l1, bdr && i == r1, true) % MOD;
        ans = (ans + t1) % MOD;
      }
    }

    if (!bdl && !bdr && num)
      dp[pos][x] = ans;

    return ans;
  }

  int countSteppingNumbers(string low, string high) {
    memset(dp, -1, sizeof dp);
    len = SZ(high);
    s1 = low, s2 = high;
    int len1 = SZ(s1);
    if (len1 < len) {
      s1 = string(len - len1, '0') + s1;
    }
    // dbg(s1, s2);

    return dfs(0, 0, true, true, false);
  }
};

