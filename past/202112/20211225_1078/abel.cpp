class Solution {
public:
  vector<string> findOcurrences(string v, string a, string b) {
    vector<string> v1, res;
    stringstream ss(v);

    string t;
    while (ss >> t) {
      v1.push_back(t);
    }

    for (int i = 2; i < v1.size(); ++i) {
      if (v1[i - 2] == a && v1[i - 1] == b) {
        res.push_back(v1[i]);
      }
    }

    return res;
  }
};
