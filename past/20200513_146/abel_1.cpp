class LRUCache {
public:
    using PII = pair<int, int>;
    map<int, list<PII>::iterator> m;
    list <PII> L;
    int cap;

    LRUCache(int capacity) : cap(capacity) {}

    int get(int key) {
        auto it = m.find(key);
        if (it == m.end()) return -1;
        PII p = *(it->second);
        L.erase(it->second);
        L.push_front(p);
        m[key] = L.begin();
        return p.second;
    }

    void put(int key, int value) {
        if (get(key) != -1) { m[key]->second = value; return; }
        if (m.size() == cap) {
            int k = L.back().first;
            L.pop_back();
            m.erase(k);
        }
        L.push_front(PII(key, value));
        m[key] = L.begin();
    }
};

