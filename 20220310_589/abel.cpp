class Solution {
public:
  VI res;

  vector<int> preorder(Node* root) {
    res = VI{};
    dfs(root);
    return res;
  }

  void dfs(Node *r) {
    if (!r) return;
    res.push_back(r->val);

    for (auto &c : r->children) {
      dfs(c);
    }
  }
};
