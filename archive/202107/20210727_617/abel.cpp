typedef ListNode LN;
typedef LN* LNP;
typedef TreeNode TN;
typedef TN* TNP;

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
  void dfs(TNP ro1, TNP ro2) {
    if (ro1 && ro2) {
      ro1->val += ro2->val;

      auto l1 = ro1->left, r1 = ro1->right,
        l2 = ro2->left, r2 = ro2->right;

      if (l1 && l2) dfs(l1, l2);
      else if (!l1) ro1->left = l2;

      if (r1 && r2) dfs(r1, r2);
      else if (!r1) ro1->right = r2;
    }
  }

  TreeNode* mergeTrees(TreeNode* ro1, TreeNode* ro2) {
    if (!ro1) return ro2;
    if (!ro2) return ro1;

    dfs(ro1, ro2);

    return ro1;
  }
};
