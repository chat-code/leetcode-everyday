class Solution {
public:
  vector<int> corpFlightBookings(vector<vector<int>>& a, int n) {
    VI res(n, 0), d(n + 1, 0);
    for (auto &v : a) {
      int x = v[0] - 1, y = v[1] - 1, c = v[2];
      d[x] += c;
      d[y + 1] -= c;
    }
    res[0] = d[0];
    for (int i = 1; i < n; ++i) {
      res[i] = res[i - 1] + d[i];
    }
    return res;
  }
};
