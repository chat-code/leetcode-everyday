/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  LNP he;

  Solution(ListNode* head) {
    he = head;
  }

  int getRandom() {
    if (!he) return -1;

    LNP p = he;
    int res = p->val, cnt = 1;
    p = p->next;

    while (p) {
      ++cnt;
      int r = rand() % cnt + 1;
      if (r == cnt) {
        res = p->val;
      }

      p = p->next;
    }

    return res;
  }
};

