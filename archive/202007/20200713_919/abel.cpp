class CBTInserter {
public:
    CBTInserter(TreeNode *root) : head(root) {
    }

    int insert(int v) {
        TreeNode *t = head;
        queue < TreeNode * > que;
        que.push(t);
        int res;
        TreeNode *v1 = new TreeNode(v);
        TreeNode *tmp;
        while (!que.empty()) {
            tmp = que.front();
            que.pop();
            if (tmp->left == nullptr) {
                tmp->left = v1;
                res = tmp->val;
                return res;
            } else if (tmp->right == nullptr) {
                tmp->right = v1;
                res = tmp->val;
                return res;
            }
            que.push(tmp->left);
            que.push(tmp->right);
        }
        return res;
    }

    TreeNode *get_root() {
        return head;
    }

private:
    TreeNode *head;
};

