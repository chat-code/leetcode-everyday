class NumArray {
public:
  VI d;

  NumArray(vector<int>& a) {
    int n = a.size();
    d = VI(n + 1, 0);
    for (int i = 1; i <= n; ++i) d[i] = a[i - 1] + d[i - 1];
  }

  int sumRange(int l, int r) {
    ++l; ++r;
    return d[r] - d[l - 1];
  }
};

